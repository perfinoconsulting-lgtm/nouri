export const MOTS: Mot[] = THEMES.flatMap((t) => t.mots)

// ─── Fonctions utilitaires ───────────────────────────────────────────────────

export function getThemeBySlug(slug: string): Theme | undefined {
  return THEMES.find((t) => t.slug === slug)
}

export function getMotsByTheme(slug: string): Mot[] {
  return THEMES.find((t) => t.slug === slug)?.mots ?? []
}

export function getMotById(id: string): Mot | undefined {
  return MOTS.find((m) => m.id === id)
}
