export function getSyllabesByLettre(lettreIndex: number): Syllabe[] {
  return SYLLABES.filter((s) => s.lettreIndex === lettreIndex)
}

export function getSyllabeById(id: string): Syllabe | undefined {
  return SYLLABES.find((s) => s.id === id)
}

export function getSyllabesForSession(lettreIndex: number): Syllabe[] {
  const lettreSyllabes = getSyllabesByLettre(lettreIndex)
  const order = ['fatha', 'kasra', 'damma', 'sukun']
  return [...lettreSyllabes].sort((a, b) => order.indexOf(a.voyelle) - order.indexOf(b.voyelle))
}
